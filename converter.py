
def convert_month(month):
    if month == 'فروردین':
        return 1
    elif month == 'اردیبهشت':
        return 2
    elif month == 'خرداد':
        return 3
    elif month == 'تیر':
        return 4
    elif month == 'مرداد':
        return 5
    elif month == 'شهریور':
        return 6
    elif month == 'مهر':
        return 7
    elif month == 'آبان':
        return 8
    elif month == 'آذر':
        return 9
    elif month == 'دی':
        return 10
    elif month == 'بهمن':
        return 11
    elif month == 'اسفند':
        return 12